<script lang="ts">
	import Button from '$lib/components/ui/Button.svelte';
	import Popover from '$lib/components/ui/Popover.svelte';
	import { onMount } from 'svelte';
	import { steps } from '$lib/data/steps';
	let alignment: 'left' | 'right' = 'right';
	const origin = $derived(alignment === 'left' ? 'origin-top-left' : 'origin-top-right');

	onMount(() => {
		const mediaQuery = window.matchMedia('(min-width: 768px)');
		const updateAlignment = (e: MediaQueryList | MediaQueryListEvent) => {
			alignment = e.matches ? 'left' : 'right';
		};

		updateAlignment(mediaQuery);
		mediaQuery.onchange = updateAlignment;

		return () => {
			mediaQuery.onchange = null;
		};
	});
</script>

<Popover let:open>
	<Button
		slot="trigger"
		variant="secondary"
		cursor="alias"
		classNames="transition-all duration-200 hover:scale-[1.02] hover:shadow-md hover:border-slate-300"
	>
		How It Works
	</Button>

	<div class={`w-[280px] max-w-[calc(100vw-2rem)] p-4 md:w-[480px] ${origin} cursor-text`}>
		<div class="space-y-6">
			{#each steps as step, i}
				<div class="flex items-start gap-4 text-sm sm:text-base" class:opacity-0={!open}>
					<div class="rounded-lg bg-blue-50 p-2">
						<step.icon class="h-6 w-6 text-blue-400" />
					</div>
					<div>
						<h3 class="mb-1 font-medium text-slate-900">
							{step.title}
						</h3>
						<p class="text-sm text-slate-600">
							{step.desc}
						</p>
					</div>
				</div>
			{/each}
		</div>
	</div>
</Popover>
